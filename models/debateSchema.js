const mongoose = require('mongoose');

const debateSchema = new mongoose.Schema({
    title: {
        type: String,
        required: true,
    },
    description: {
        type: String,
        required: true,
    },
    date: {
        type: String,
        required: true,
    },
    time: {
        type: String,
        required: true,
    },
    location: {
        type: String,
        required: true,
    }
});

const Debate = mongoose.model('Debate', debateSchema);
module.exports = Debate;